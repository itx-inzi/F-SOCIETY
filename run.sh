        print_color $YELLOW "   sudo apt-get install git   # Debian/Ubuntu"
        print_color $YELLOW "   brew install git           # macOS"
        print_color $YELLOW "   apt install git            # Termux"
        print_color $YELLOW "   yum install git            # CentOS/RHEL"
